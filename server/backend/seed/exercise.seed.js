/* ---------- Expand to 100+ Exercises ---------- */
const exercises = [...baseExercises];

for (let i = 1; i <= 5; i++) {
  baseExercises.forEach((e) => {
    exercises.push({
      ...e,
      name: `${e.name} Variation ${i}`,
      calories: Math.round(e.calories * (1 + i * 0.05)),
      xpReward: {
        low: e.xpReward.low + i,
        medium: e.xpReward.medium + i * 2,
        high: e.xpReward.high + i * 3,
      },
    });
  });
}

/* ---------- Seed ---------- */
const seed = async () => {
  await Exercise.deleteMany();
  await Exercise.insertMany(exercises);
  console.log(`🔥 Seeded ${exercises.length} exercises`);
  process.exit();
};

seed();
